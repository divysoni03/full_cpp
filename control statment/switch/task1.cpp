// 1. Get character as input and check whether vowel / consonant Using SWITCH CASE
// Output :-
// Enter any character : c
// Letter is consonant

#include<iostream>
using namespace std;

int main(){
    char ch;
    cout << "\nEnter any character : ";
    cin >> ch;
    switch(ch){
        a : {
            cout << "\nLetter is vowel";
        }
        e : {
            cout << "\nLetter is vowel";
        }
        i : {
            cout << "\nLetter is vowel";
        }
        o : {
            cout << "\nLetter is vowel";
        }
        u : {
            cout << "\nLetter is vowel";
        }
        default : {
            cout << "\nLetter is consonant";
        }
    }
}